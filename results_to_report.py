#!/usr/bin/env python3
"""
results_to_report.py
====================
Reads the CSV files generated by run_all_benchmarks.sh and prints
a clean summary table for each database — ready to copy into the report.

Usage:
    python3 results_to_report.py

CSV format expected (one file per database):
    Workload, Records, Threads, AvgRuntime_ms, AvgRuntime_s, Throughput_ops_sec
"""

import csv
import os

DBS = {
    "ravendb":  "RavenDB",
    "mongodb": "MongoDB",
    "couchdb":  "CouchDB",
    "mysql":    "MySQL",
}

WORKLOADS = ["workloada","workloadb","workloadc","workloadd",
             "workloade","workloadf","workloadg","workloadh"]

RECORDS  = [100_000, 1_000_000, 10_000_000]
THREADS  = [1, 3, 6]

RESULTS_DIR = "./results"

def load_csv(db):
    path = os.path.join(RESULTS_DIR, f"{db}_results.csv")
    if not os.path.exists(path):
        return None
    data = {}
    with open(path) as f:
        for row in csv.DictReader(f):
            try:
                if not row.get("Workload") or not row.get("Records") or not row.get("Threads"):
                    continue
                key = (row["Workload"], int(row["Records"]), int(row["Threads"]))
                data[key] = {
                    "ms":  row.get("AvgRuntime_ms", "0"),
                    "s":   row.get("AvgRuntime_s", "0"),
                    "ops": row.get("Throughput_ops_sec", "0"),
                }
            except (ValueError, TypeError):
                continue
    return data if data else None

def print_table(db_label, data, metric="s"):
    """Print a workload × (records+threads) table."""
    combos = [(r, t) for r in RECORDS for t in THREADS]
    col_w = 14

    # Header
    header_label = {
        "s":   "Runtime (seconds)",
        "ms":  "Runtime (ms)",
        "ops": "Throughput (ops/sec)",
    }[metric]

    print(f"\n{'='*80}")
    print(f"  {db_label}  —  {header_label}")
    print(f"{'='*80}")

    # Column headers
    hdr = "Workload".ljust(12)
    for r, t in combos:
        label = f"{r//1000}K/{t}T"
        hdr += label.rjust(col_w)
    print(hdr)
    print("─" * len(hdr))

    totals = {(r,t): 0.0 for r,t in combos}

    for wl in WORKLOADS:
        row_str = wl.replace("workload","WL-").ljust(12)
        for r, t in combos:
            val = data.get((wl, r, t), {}).get(metric, "—")
            row_str += str(val).rjust(col_w)
            try:
                totals[(r,t)] += float(val)
            except (ValueError, TypeError):
                pass
        print(row_str)

    # Totals row
    print("─" * len(hdr))
    tot_str = "TOTAL".ljust(12)
    for r, t in combos:
        tot = round(totals[(r,t)], 2) if metric in ("s","ms") else "—"
        tot_str += str(tot).rjust(col_w)
    print(tot_str)

def print_comparison_table(all_data):
    """Print total runtime comparison across all databases."""
    print(f"\n{'='*80}")
    print("  OVERALL COMPARISON  —  Total Runtime (seconds, all workloads)")
    print(f"{'='*80}")

    combos = [(r, t) for r in RECORDS for t in THREADS]
    col_w = 14

    hdr = "Database".ljust(12)
    for r, t in combos:
        label = f"{r//1000}K/{t}T"
        hdr += label.rjust(col_w)
    print(hdr)
    print("─" * len(hdr))

    for db, label in DBS.items():
        data = all_data.get(db)
        if data is None:
            continue
        row_str = label[:11].ljust(12)
        for r, t in combos:
            total = sum(
                float(data.get((wl, r, t), {}).get("s", 0) or 0)
                for wl in WORKLOADS
            )
            row_str += str(round(total, 2)).rjust(col_w)
        print(row_str)

def main():
    print("\n" + "╔" + "═"*50 + "╗")
    print("║   YCSB Results Summary — Ready for Report    ║")
    print("╚" + "═"*50 + "╝")

    all_data = {}
    missing  = []

    for db, label in DBS.items():
        data = load_csv(db)
        if data is None:
            missing.append(label)
            continue
        all_data[db] = data
        print_table(label, data, metric="s")   # seconds table

    if missing:
        print(f"\n⚠️  Missing results for: {', '.join(missing)}")
        print("   Run run_all_benchmarks.sh first for those databases.")

    if len(all_data) > 1:
        print_comparison_table(all_data)

    print("\n" + "─"*80)
    print("TIP: Copy the table values above into your report.")
    print("     Each row = one workload, each column = records/threads combo.")
    print("     The TOTAL row = sum of all workloads (use for Tables 4–9).")
    print("─"*80 + "\n")

if __name__ == "__main__":
    main()